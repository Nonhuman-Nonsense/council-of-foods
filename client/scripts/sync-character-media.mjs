/**
 * Reads forest character image/video dimensions from client/src/assets/characters,
 * verifies all video encodes share one aspect ratio, and writes
 * src/generated/characterMedia.ts.
 *
 * Video-type characters use large/ and small/ encodes (HEVC + VP9). Image-type
 * characters use images/*.avif.
 */
import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import ffprobe from "@ffprobe-installer/ffprobe";
import imageSize from "image-size";

const __dirname = dirname(fileURLToPath(import.meta.url));
const clientRoot = join(__dirname, "..");
const sharedRoot = join(clientRoot, "../shared");
const characterAssets = join(clientRoot, "src", "assets", "characters");
const sceneManifestPath = join(sharedRoot, "prompts", "forest_characters.json");
const outPath = join(clientRoot, "src", "generated", "characterMedia.ts");

const entries = JSON.parse(readFileSync(sceneManifestPath, "utf8"));
if (!Array.isArray(entries)) {
  throw new Error(`${sceneManifestPath} must be a JSON array.`);
}

// Allow tiny drift between codecs and large/small encodes; still catches materially wrong aspect ratios.
const RATIO_EPS = 0.005;
const RATIO_IDENTICAL_EPS = 1e-12;

function filenameId(id) {
  return String(id).toLowerCase().replaceAll(" ", "_");
}

function getImageDimensions(absPath) {
  const buf = readFileSync(absPath);
  const result = imageSize(buf);
  if (!result.width || !result.height) {
    throw new Error(`Could not read dimensions for image: ${absPath}`);
  }
  return { width: result.width, height: result.height };
}

function getVideoDimensions(absPath) {
  const out = execFileSync(
    ffprobe.path,
    [
      "-v",
      "error",
      "-select_streams",
      "v:0",
      "-show_entries",
      "stream=width,height",
      "-of",
      "csv=p=0",
      absPath,
    ],
    { encoding: "utf8", maxBuffer: 10 * 1024 * 1024 },
  );
  const line = out.trim().split("\n")[0];
  if (!line) {
    throw new Error(`ffprobe produced no output for: ${absPath}`);
  }
  const [width, height] = line.split(",").map((value) => parseInt(value, 10));
  if (!Number.isFinite(width) || !Number.isFinite(height) || width <= 0 || height <= 0) {
    throw new Error(`Invalid video dimensions for ${absPath}: ${line}`);
  }
  return { width, height };
}

function ratioKey(width, height) {
  return width / height;
}

function formatDimensionLines(entries) {
  return entries
    .map(({ path, width, height, ratio }) => `  ${path}  ->  ${width} / ${height} = ${ratio}`)
    .join("\n");
}

function assertSameRatio(id, label, pairs) {
  const entries = pairs.map(({ path, width, height }) => ({
    path,
    width,
    height,
    ratio: ratioKey(width, height),
  }));
  const base = entries[0].ratio;
  let maxDiff = 0;
  for (let i = 1; i < entries.length; i++) {
    maxDiff = Math.max(maxDiff, Math.abs(entries[i].ratio - base));
  }

  const lines = formatDimensionLines(entries);

  if (maxDiff > RATIO_EPS) {
    console.error(
      `\n[sync-character-media] Aspect ratio mismatch for "${id}" (${label}) (exceeds RATIO_EPS=${RATIO_EPS}):\n` +
        lines +
        `\n  reference ratio (first file) ≈ ${base}\n  max |Δratio| ≈ ${maxDiff}\n`,
    );
    process.exit(1);
  }

  if (maxDiff > RATIO_IDENTICAL_EPS) {
    console.warn(
      `[sync-character-media] Warning: "${id}" (${label}) — aspect ratios differ slightly but stay within RATIO_EPS (${RATIO_EPS}). ` +
        `max |Δratio| ≈ ${maxDiff}. Check encodes.\n` +
        lines +
        `\n  reference (first file) ≈ ${base}\n`,
    );
  }
}

function writeOutput(dimensions, ratios) {
  const ids = Object.keys(ratios).sort();
  const dimensionLines = ids.map(
    (id) => `  ${JSON.stringify(id)}: { width: ${dimensions[id].width}, height: ${dimensions[id].height} },`,
  );
  const ratioLines = ids.map((id) => `  ${JSON.stringify(id)}: ${ratios[id]},`);

  const ts = `// Generated by scripts/sync-character-media.mjs — do not edit by hand.
// Run: npm run build (prebuild) or npm run dev (predev)

export type CharacterDimensions = { width: number; height: number };

export const characterDimensions: Record<string, CharacterDimensions> = {
${dimensionLines.join("\n")}
};

export const characterRatios: Record<string, number> = {
${ratioLines.join("\n")}
};
`;

  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, ts, "utf8");
  console.log(`[sync-character-media] Wrote ${outPath} (${ids.length} characters).`);
}

function main() {
  /** @type {Record<string, { width: number; height: number }>} */
  const dimensions = {};
  /** @type {Record<string, number>} */
  const ratios = {};

  for (const entry of entries) {
    const id = entry.id;
    const basename = filenameId(id);

    if (entry.type === "image") {
      const relPath = join("images", `${basename}.avif`);
      const absPath = join(characterAssets, relPath);
      try {
        const { width, height } = getImageDimensions(absPath);
        dimensions[id] = { width, height };
        ratios[id] = ratioKey(width, height);
      } catch (error) {
        console.error(
          `[sync-character-media] Failed reading image for "${id}" (${absPath}):`,
          error.message || error,
        );
        process.exit(1);
      }
      continue;
    }

    if (entry.type === "video") {
      const relPaths = [
        join("large", `${basename}-vp9-chrome.webm`),
        join("large", `${basename}-hevc-safari.mp4`),
        join("small", `${basename}-vp9-chrome.webm`),
        join("small", `${basename}-hevc-safari.mp4`),
      ];
      const measured = [];
      for (const relPath of relPaths) {
        const absPath = join(characterAssets, relPath);
        try {
          const { width, height } = getVideoDimensions(absPath);
          measured.push({ path: relPath, width, height });
        } catch (error) {
          console.error(
            `[sync-character-media] Failed reading video for "${id}" (${absPath}):`,
            error.message || error,
          );
          process.exit(1);
        }
      }
      assertSameRatio(id, `${relPaths.length} encode(s)`, measured);
      const { width, height } = measured[0];
      dimensions[id] = { width, height };
      ratios[id] = ratioKey(width, height);
      continue;
    }

    console.error(`Unknown type for "${id}": ${entry.type}`);
    process.exit(1);
  }

  writeOutput(dimensions, ratios);
}

main();
